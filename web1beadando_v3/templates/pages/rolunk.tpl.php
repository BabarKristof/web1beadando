<h1>Küldetésünk</h1>

<div id="t2">
<p>
<i>"Bárcsak megértenék az emberek, hogy az állatok ugyanolyan kiszolgáltatottak, s ugyanúgy függenek tőlünk, mint a gyerekek; ezt a gyámságot ránk ruházták. "</i>
 - <b>James Herriot</b>
(1916.10.03. - 1995.02.23., brit állatorvos, író)
</p>
<p>
Mi a Tappancsnál hiszünk abban, hogy eljön az az idő, amikor már nem lesz szükség állatmenhelyekre, mert minden kedvtelésből tartott állat egész életét szerető családban töltheti.

Hiszünk az emberi jóságban, hiszünk abban, hogy a felvilágosító tevékenységünk megváltoztatja a gondolkodást.

Mi a Tappancsnál elkötelezettek vagyunk azért, hogy értéket teremtsünk. A hétköznapokban a tevékenységünket az állatszeretet, a csapatmunka ereje és a felelősségtudat vezérli.

Felelősséget érzünk azért, hogy támogatóink közös akaratát megvalósítsuk, és olyan állatokat mentsünk, akik rajtunk kívül másra már nem számíthatnak.

Elhivatottak vagyunk a Tappancs Tanyára befogadott állatok jólétéért, biztonságáért és boldogságáért, hogy volt gazdájuk helyett felelős gazdái legyünk egészen addig, amíg minden egyes védencünknek megtaláljuk a számukra leginkább megfelelő családot.
</p>
